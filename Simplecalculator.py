a=int(input(" enter a value :"))
b=int(input(" enter b value :"))
operator=input(" enter the operator:")
if operator=='+':
        print(a+b)
elif operator =='-':
      print(a-b)
elif operator == '/':
      print(a/b)
elif operator == '*':
      print(a*b)
elif operator == '**':
      print(a**b)
elif operator == '%':
      print(a%b)
elif operator =='//':
      print(a//b)
else:
    print("Invalid operator! enter a valid operator")
     